TileMap::TileMap()
{
	_dirt = TextureManager::LoadTexture("assets\\dirt.png");
	_grass = TextureManager::LoadTexture("assets\\grass.png");
	_water = TextureManager::LoadTexture("assets\\water.png");

	LoadMap(level1);

	_src.x = 0;
	_src.y = 0;
	_src.w = 32;
	_src.h = 32;
	_dest.x = 0;
	_dest.y = 0;
	_dest.w = 32;
	_dest.h = 32;

}

void TileMap::LoadMap(int mapArray[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			_map[row][column] = mapArray[row][column];
		}
	}
}

void TileMap::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int column = 0; column < 25; column++)
		{
			type = _map[row][column];

			_dest.x = column * 32;
			_dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(_water, _src, _dest);
				break;
			case 1:
				TextureManager::Draw(_grass, _src, _dest);
				break;
			case 2:
				TextureManager::Draw(_dirt, _src, _dest);
			default:
				break;
			}
				
		}
	}
}